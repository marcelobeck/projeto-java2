package Secao3Fundamentos;

public class ConversaoTipoPrimitivo01 {

    public static void main(String[] args) {

        double a = 1; // implícita
        System.out.println(a);

        float b = (float) 1.9854; // explícita (CAST)
        System.out.println(b);

    }

    // Byte, Short, int, long

    /*
     Conversão de byte para short / short para int / int para long, não perder informação
     Essa forma é a Implicita

     de short para byte é a forma Explicita
    */

    /*
       inteiro -> float, o correto, conversão Implicita (não perde informação)
       float -> inteiro, pode da erro, conversão Explicita (pode perder informação)
     */





}
